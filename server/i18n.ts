export const SUPPORTED_LOCALES = ["en", "es", "fr"] as const;
export type Locale = (typeof SUPPORTED_LOCALES)[number];

export function t(key: string, locale: Locale = "en"): string {
  return translations[locale]?.[key] || translations.en[key] || key;
}

export function getTranslations(locale: Locale): Record<string, string> {
  return { ...translations.en, ...(translations[locale] || {}) };
}

export function formatDate(date: string | Date, locale: Locale = "en"): string {
  const d = typeof date === "string" ? new Date(date) : date;
  const localeMap: Record<string, string> = { en: "en-US", es: "es-ES", fr: "fr-FR" };
  return d.toLocaleDateString(localeMap[locale] || "en-US", { year: "numeric", month: "2-digit", day: "2-digit" });
}

export function formatCurrency(amount: number, currency: string = "USD", locale: Locale = "en"): string {
  const localeMap: Record<string, string> = { en: "en-US", es: "es-ES", fr: "fr-FR" };
  return new Intl.NumberFormat(localeMap[locale] || "en-US", { style: "currency", currency }).format(amount);
}

export function formatNumber(num: number, locale: Locale = "en"): string {
  const localeMap: Record<string, string> = { en: "en-US", es: "es-ES", fr: "fr-FR" };
  return new Intl.NumberFormat(localeMap[locale] || "en-US").format(num);
}

export function getDirection(locale: Locale): "ltr" | "rtl" {
  return "ltr";
}
